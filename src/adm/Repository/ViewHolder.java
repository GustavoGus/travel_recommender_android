package adm.Repository;

import android.widget.TextView;

/**
 * @author Rafa
 *
 */
public class ViewHolder {
	
	private TextView titulo;
	private TextView subtitulo;
	private TextView subsubtitulo;
	private int idPlace;
	private String typePlace;
	
	/**
	 * @param titulo
	 * @param subtitulo
	 * @param subsubtitulo
	 * @param idPlace
	 * @param typePlace
	 */
	public ViewHolder(TextView titulo, TextView subtitulo,
			TextView subsubtitulo, int idPlace, String typePlace) {
		super();
		this.titulo = titulo;
		this.subtitulo = subtitulo;
		this.subsubtitulo = subsubtitulo;
		this.idPlace = idPlace;
		this.typePlace=typePlace;
	}
	
	public ViewHolder() {
		this.titulo = null;
		this.subtitulo = null;
		this.subsubtitulo = null;
		this.idPlace = -1;
		this.typePlace="";
	}

	/**
	 * @return the titulo
	 */
	public TextView getTitulo() {
		return titulo;
	}
	/**
	 * @param titulo the titulo to set
	 */
	public void setTitulo(TextView titulo) {
		this.titulo = titulo;
	}
	/**
	 * @return the subtitulo
	 */
	public TextView getSubtitulo() {
		return subtitulo;
	}
	/**
	 * @param subtitulo the subtitulo to set
	 */
	public void setSubtitulo(TextView subtitulo) {
		this.subtitulo = subtitulo;
	}
	/**
	 * @return the subsubtitulo
	 */
	public TextView getSubsubtitulo() {
		return subsubtitulo;
	}
	/**
	 * @param subsubtitulo the subsubtitulo to set
	 */
	public void setSubsubtitulo(TextView subsubtitulo) {
		this.subsubtitulo = subsubtitulo;
	}
	/**
	 * @return the idPlace
	 */
	public int getIdPlace() {
		return idPlace;
	}
	/**
	 * @param idPlace the idPlace to set
	 */
	public void setIdPlace(int idPlace) {
		this.idPlace = idPlace;
	}

	/**
	 * @return the typePlace
	 */
	public String getTypePlace() {
		return typePlace;
	}

	/**
	 * @param typePlace the typePlace to set
	 */
	public void setTypePlace(String typePlace) {
		this.typePlace = typePlace;
	}
	
}
