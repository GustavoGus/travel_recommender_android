package adm.Comment;


public class CommentResponse {
	private int status;
	private Comments result;
	
	public CommentResponse(int status, Comments result) {
		super();
		this.status = status;
		this.result = result;
	}
	
	/*
	 * Getters and Setters
	 */
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
	/**
	 * @return the result
	 */
	public Comments getResult() {
		return result;
	}
	/**
	 * @param result the result to set
	 */
	public void setResult(Comments result) {
		this.result = result;
	}
	
}
