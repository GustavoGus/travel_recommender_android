package adm.Submit;

public class SimpleResponse {
	private int status;
	
	/*
	 * Getters and Setters
	 */
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	/*
	 * Constructor
	 */
	public SimpleResponse(int status) {
		super();
		this.status = status;
	}
}
